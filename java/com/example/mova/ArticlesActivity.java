    @Override
    protected void onCreate(Bundle savedInstanceState) {
        super.onCreate(savedInstanceState);
        setContentView(R.layout.activity_articles);

        ImageButton btnBack = findViewById(R.id.btnBack);
        btnBack.setOnClickListener(v -> finish());

        ListView listView = findViewById(R.id.listViewArticles);

        // Используем кастомный адаптер вместо стандартного
        ArticleListAdapter adapter = new ArticleListAdapter(this, articleTitles, articleContents);
        listView.setAdapter(adapter);

        listView.setOnItemClickListener((parent, view, position, id) -> {
            Intent intent = new Intent(ArticlesActivity.this, ArticleDetailActivity.class);
            intent.putExtra("title", articleTitles[position]);
            intent.putExtra("content", articleContents[position]);
            startActivity(intent);
        });
    }

    @Override
    protected void onResume() {
        super.onResume();
        // Обновляем статусы прочтения при возвращении на эту страницу
        ListView listView = findViewById(R.id.listViewArticles);
        ArticleListAdapter adapter = (ArticleListAdapter) listView.getAdapter();
        if (adapter != null) {
            adapter.notifyDataSetChanged();
        }
    }
